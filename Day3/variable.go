package main
import "fmt"
func main() {
	var a = "Apple Computer";
	fmt.Println(a);
	var b int = 10;
	fmt.Println(b);
	//Here we declare a int variable but didn't give any value in that case its value is zero as default value. 
	var c int ;
	fmt.Println(c);
	var d = false;
	fmt.Println(d);
	//A way to initialize and declare a variable;
	e:= false;
	fmt.Println(e);
}